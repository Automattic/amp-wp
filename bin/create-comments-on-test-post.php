/**
 * Create comments test post.
 *
 * @throws Exception But when database doesn't have enough attachments or in case of error.
 * @return int Post ID.
 */
function amp_create_comments_test_post() {
	$q = new WP_Query( array( 'name' => 'amp-test-comments' ) );
	if ( $q->have_posts() ) {
		$post    = $q->next_post();
		$post_id = $post->ID;
	} else {
		$post_id = wp_insert_post( array(
			'post_name'   => 'amp-test-comments',
			'post_title'  => 'AMP Test Comments',
			'post_type'   => 'post',
			'post_status' => 'publish',
		) );

		if ( ! $post_id || is_wp_error( $post_id ) ) {
			throw new Exception( 'The test post could not be added, please try again.' );
		}
	}

	$content = wp_remote_get( 'https://loripsum.net/api/10/short/headers' );
	if ( is_wp_error( $content ) ) {
		throw new Exception( 'Could not get test content, please try again.' );
	}
	$update = wp_update_post( wp_slash( array(
		'ID'           => $post_id,
		'post_content' => wp_remote_retrieve_body( $content ),
	) ) );

	if ( ! $update ) {
		throw new Exception( 'The test post could not be updated, please try again.' );
	} else {
		amp_add_comments( $update );
	}

	return $update;
}

/**
 * Add Comments to the post.
 *
 * @throws Exception If a comment can't be added.
 * @param int $post_id The post ID to add comments to.
 */
function amp_add_comments( $post_id ) {
	$data = amp_get_test_comment_entries();
	$ids  = array();
	// get some paragraphs.
	$content = array_filter( file( 'https://loripsum.net/api/' . count( $data ) . '/short' ), function ( $s ) {
		return ! empty( trim( $s ) );
	} );
	sort( $content );
	$content = array_filter( $content );
	foreach ( $data as $inx => $comment ) {
		$comment['comment_post_ID'] = $post_id;
		$yes_no                     = rand( 0, 1 );
		// 50% chance of this comment being a reply :)
		if ( 1 === $yes_no && ! empty( $ids ) ) {
			$comment['comment_parent'] = $ids[ rand( 0, count( $ids ) - 1 ) ];
		}
		$comment['comment_content'] = $content[ $inx ];
		$comment_id                 = wp_insert_comment( $comment );
		if ( ! $comment_id || is_wp_error( $comment_id ) ) {
			throw new Exception( 'The test comment could not be added, please try again.' );
		}
		$ids[] = $comment_id;
	}
}

